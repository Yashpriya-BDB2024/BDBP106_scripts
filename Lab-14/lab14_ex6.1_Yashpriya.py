#Get the length from the input
length=int(input("Enter the length of the rectangle: "))
#Also, get the breadth from the input
breadth=int(input("Enter the breadth of the rectangle: "))
#Calculate the perimeter of the rectangle
perimeter=2*(length+breadth)
#Calculate the area of the rectangle
area=length*breadth
#Print the final statement displaying the output values of above two operations
print("The perimeter and the area of the rectangle with length", length,"cm and breadth", breadth,"cm is", perimeter,"cm and", area,"sq.cm")
