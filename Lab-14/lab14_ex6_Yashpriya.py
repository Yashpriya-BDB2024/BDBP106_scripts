#Get the radius as the input
radius=int(input("Enter the radius: "))
#Calculate the diameter
diameter=radius*2
print("The diameter of the circle with radius", radius,"cm", "is", diameter,"cm") 
#Set the value of 'pi'
pi=3.14
#Calculate the circumference of the circle
Circumference=2*pi*radius 
print("The circumference of the circle with radius", radius,"cm", "is", Circumference,"cm")
#Calculate the area of the circle
Area=pi*radius**2
print("The area of the circle with radius", radius,"cm is", Area,"sq.cm")
